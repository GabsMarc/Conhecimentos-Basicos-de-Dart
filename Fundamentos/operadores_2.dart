
main() {
   
  // Operadores Atribuição
  
  double a = 3;
  a = a + 2; // Acrescenta ao valor da variavel a, + 3.
  a += 5; // adição somativa - Também acrescenta valor a variavél a; 
  a -= 5; // adição subtativa - Também decrementa valor a variavél a; 
  a *= 5;
  a/= 5;  

  print(a); 


  //Operadores Relacionais (Valor sempre vai ser bool)

  print(3 > 2);  // Maior que
  print(3 >= 3); // Maior ou Igual  
  print(3 < 4);  // Menor que
  print(3 <= 3); // Menor ou igual
  print(3 != 3); // Diferente de
  print(3 == 3); // Igual






}